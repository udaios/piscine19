int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int res;

	res = 0;
	i = 0;
	while (i <= n)
	{
		res = s1[i] - s2[i];
		i++;
	}
	if (res > 0)
		return (1);
	else if (res == 0)
		return (0);
	else
		return (-1);
}
