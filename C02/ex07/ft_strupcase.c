#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}

int		main(void)
{
	char a[5] = {'a', 'U', 'p', 'T', 'r'};

	ft_strupcase(a);
	printf("%s", a);
	return (0);
}
